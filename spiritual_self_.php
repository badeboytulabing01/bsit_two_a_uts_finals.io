             <strong>2.
            What are the challenges, issues, or problems that college students experience
            about their spiritual self? </strong>
          </p>
          <p>
            Students in college have to adjust to a new physical, social, and academic
environment. According to a growing body of research, students' social identities
(Tajfel and Turner, 1979) and the psychological factors connected to those identities
can influence how they feel about themselves and where they belong both during
and after their time in college (Walton and Cohen, 2007, 2011; Strayhorn, 2012; Brady
et al., 2020). Racial or ethnic background, gender, or being a first-generation college
student have been the main topics of research on how social identities affect students'
outcomes in college (Walton and Cohen, 2011; Stephens et al., 2012; Walton et al.,
2015; Yeager et al., 2016. Further research is necessary into various facets of students'
identities. Here, we put a special emphasis on students' sense of their spiritual
identity, particularly how much they feel near to God and how much they struggle
spiritually. We investigate the relationships between these spiritual variables and two
other crucial early college outcomes: wellbeing and belonging.
</p>
<p>
  We use the description offered by Exline et al. (2014), who characterize
spiritual challenges as unfavorable thoughts or conflict related to a particular
component of spirituality (such as beliefs, practices, or experiences). Spiritual conflicts
can occur internally or with other individuals or institutions over morality, doubt, or
the ultimate purpose of existence. They can also occur with supernatural beings (gods
or devils), other people or institutions over religious or spiritual issues.
</p>
<p>
  College students frequently have spiritual problems when they reevaluate
their religious and spiritual convictions outside of their family and community
environments (Barry and Abo-Zena, 2014). For instance, students may struggle to
reconcile their religious beliefs with the new information they have learned in their
academic classes. On campus, there may be conflicts between students from various
religious backgrounds. Or, if their spiritual challenges cause them to disagree with
other believers or if the context of the religious community discourages conversation
about existential questions, ultimate meaning, and doubt (Bryant and Astin, 2008),
students may find themselves at odds with other believers within their religion
tradition.
</p>
